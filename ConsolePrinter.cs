        public static void PrintPreferredPlayStyle(RaceType race)
        {
            switch (race)
            {
                case RaceType.Elf:
                    Console.WriteLine("Preferred play-style for elf is Mage.");
                    break;
                case RaceType.Orsimer:
                    Console.WriteLine("Preferred play-style for orsimer is Warrior.");
                    break;
                case RaceType.Man:
                    Console.WriteLine("Preferred play-style for man is Rogue.");
                    break;
            }
        }

        public static void PrintClass(ClassType classType)
        {
            switch (classType)
            {
                case ClassType.Mage:
                    Console.WriteLine("Do you want to explore secret of magic? Good choice!");
                    ConsolePrinter.PrintMage();
                    break;
                case ClassType.Warrior:
                    Console.WriteLine("Do you want to be a master of sword? Good choice!");
                    ConsolePrinter.PrintWarrior();
                    break;
                case ClassType.Rogue:
                    Console.WriteLine("Do you want to be a master of sneaking and attack from afar? Good choice!");
                    ConsolePrinter.PrintRogue();
                    break;
                default:
                    break;
            }
        }

